        const trigger = document.querySelector('#footer-trigger'); // New target

        const observerOptions = {
            root: null,
            rootMargin: '0px 0px 200px 0px', // Footer triggers 200px before reaching the bottom
            threshold: 0.6
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    footer.classList.add('is-active');
                } else {
                    footer.classList.remove('is-active');
                }
            });
        }, observerOptions);

        if (trigger) {
            observer.observe(trigger);
        }

function getInitials(name) {
  return name
    .split(" ")
    .map((part) => part[0])
    .join("")
    .slice(0, 3)
    .toUpperCase();
}

function createOfficerList(officers) {
  return officers
    .map(
      ([role, person]) => `
        <li>
          <span>${role}</span>
          <strong>${person}</strong>
        </li>
      `
    )
    .join("");
}

function createCard(item) {
  const logoMarkup = item.logo
    ? `<img src="${item.logo}" alt="${item.name} logo">`
    : getInitials(item.name);
  const linkHref = item.link || "#";
  const linkLabel = item.link ? "VISIT FB PAGE": "NO FB PAGE";
  const linkClass = item.link ? "org-link" : "org-link is-disabled";

  return `
    <article class="org-card">
      <div class="org-logo"${item.logo ? "" : ' aria-hidden="true"'}>${logoMarkup}</div>
      <div>
        <span class="org-tag">${item.tag}</span>
        <h3 class="org-name">${item.name}</h3>
      </div>
      <p class="org-description">${item.summary}</p>
      <div class="org-meta">
        <h4>Core Officers</h4>
        <ul>${createOfficerList(item.officers)}</ul>
        <div class="org-actions">
          <a class="${linkClass}" href="${linkHref}" target="_blank" rel="noopener noreferrer">${linkLabel}</a>
        </div>
      </div>
    </article>
  `;
}

function renderSections() {
  document.querySelectorAll(".org-track").forEach((track) => {
    const sectionKey = track.dataset.section;
    const items = organizationData[sectionKey] || [];
    track.innerHTML = items.map(createCard).join("");
  });
}

function setupCarouselButtons() {
  document.querySelectorAll("[data-carousel-target]").forEach((button) => {
    button.addEventListener("click", () => {
      const target = document.getElementById(button.dataset.carouselTarget);
      const direction = button.dataset.direction === "next" ? 1 : -1;
      const scrollAmount = Math.max(target.clientWidth * 0.82, 280) * direction;

      target.scrollBy({
        left: scrollAmount,
        behavior: "smooth"
      });
    });
  });
}

